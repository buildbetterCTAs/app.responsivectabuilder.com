<template>
  <div v-show="visible">
    <slot></slot>
  </div>
</template>

<script>
  export default {
    props: ['label', 'selected'],
    template: '#tab',
    created: function () {
      var self = this

      if (typeof this.selected !== 'undefined' && this.selected === '') {
        this.$parent.$data.activeTab = this.label
        this.visible = true
      }

      this.$parent.$data.tabs.push(this.label)

      this.$parent.$on('activateTab', function (tab) {
        if (self.label === tab) {
          self.visible = true
        } else {
          self.visible = false
        }
      })
    },
    data: function () {
      return {
        visible: false
      }
    }
  }
</script>
